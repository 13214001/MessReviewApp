    private void registerViews() {
        inputFullName = (EditText) findViewById(R.id.name);
        // TextWatcher would let us check validation error on the fly
        inputFullName.addTextChangedListener(new TextWatcher() {
            public void afterTextChanged(Editable s) {
                Validation.hasText(inputFullName);
            }
            public void beforeTextChanged(CharSequence s, int start, int count, int after){}
            public void onTextChanged(CharSequence s, int start, int before, int count){}
        });
        inputPassword = (EditText) findViewById(R.id.password);
        // TextWatcher would let us check validation error on the fly
        inputPassword.addTextChangedListener(new TextWatcher() {
            public void afterTextChanged(Editable s) {
                Validation.hasText(inputPassword);
            }
            public void beforeTextChanged(CharSequence s, int start, int count, int after){}
            public void onTextChanged(CharSequence s, int start, int before, int count){}
        });

        inputEmail = (EditText) findViewById(R.id.email);
        inputEmail.addTextChangedListener(new TextWatcher() {
            // after every change has been made to this editText, we would like to check validity
            public void afterTextChanged(Editable s) {
                Validation.isEmailAddress(inputEmail, true);
            }
            public void beforeTextChanged(CharSequence s, int start, int count, int after){}
            public void onTextChanged(CharSequence s, int start, int before, int count){}
        });



        btnRegister = (Button) findViewById(R.id.btnRegister);
        btnRegister.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {

                String name = inputFullName.getText().toString();
                String email = inputEmail.getText().toString();
                String password = inputPassword.getText().toString();
                /*
                Validation class will check the error and display the error on respective fields
                but it won't resist the form submission, so we need to check again before submit
                 */
                if ( checkValidation () )
                    registerUser(name, email, password);
                else
                    Toast.makeText(RegisterActivity.this, "Form contains error", Toast.LENGTH_LONG).show();
            }
        });
    }//copied codes
   // functionCheckValidationStarts
   private boolean checkValidation() {
       boolean ret = true;

       if (!Validation.hasText(inputFullName)) ret = false;
       if (!Validation.isEmailAddress(inputEmail, true)) ret = false;
       if (!Validation.hasText(inputPassword)) ret = false;

       return ret;
   }

